 function com = pop_tesa_plotgroup( EEG, varargin )

com = '';          

%check that data is present
if isempty(EEG.data)
    error('Data is empty');
end

% pop up window
% -------------
if nargin < 2
    
    chanAll = {EEG.chanlocs.labels};
    
   if isfield(EEG,'ROI')
        roiFields = fieldnames(EEG.ROI);
        roi = 'yes';
        for a = 1:size(roiFields,1)
            roiFields{a,1} = ['ROI ',roiFields{a,1}];
        end
    else 
        roi = 'no';
    end
    if isfield(EEG,'GMFA')
        gmfaFields = fieldnames(EEG.GMFA);
        gmfa = 'yes';
        for a = 1:size(gmfaFields,1)
            gmfaFields{a,1} = ['GMFA ',gmfaFields{a,1}];
        end
    else 
        gmfa = 'no';
    end

    if strcmpi(roi,'yes') && strcmpi(gmfa,'yes')
        tepInput = ['All', chanAll, roiFields', gmfaFields'];
    elseif strcmpi(roi,'yes') && strcmpi(gmfa,'no')
        tepInput = ['All', chanAll, roiFields'];
    elseif strcmpi(roi,'no') && strcmpi(gmfa,'yes')
        tepInput = ['All', chanAll, gmfaFields'];
    elseif strcmpi(roi,'no') && strcmpi(gmfa,'no')
        tepInput = ['All', chanAll];
    end
    
    tepAll = tepInput;
    
    for a = 1:size(tepInput,1)-1
        tepInput{a,1} = [tepInput{a,1},'|'];
    end
    
    geometry = {1 [0.5 0.5] 1 1 [1 0.5] [1 0.5] 1 [1 0.5] 1 1 1};

    uilist = {{'style', 'text', 'string', 'Plot TMS-evoked potentials averaged across a group','fontweight','bold'} ...
              {'style', 'text', 'string', 'Data to plot'} ...
              {'style', 'popupmenu', 'string', tepInput 'tag' 'data' } ...
              {}...
              {'style', 'text', 'string', 'Plot options','fontweight','bold'} ...
              {'style', 'text', 'string', 'Time limits (ms) (e.g. -100,500)'} ...
              {'style', 'edit', 'string', '-100,500'} ...
              {'style', 'text', 'string', 'Amplitude limits (uV) (e.g. -30,30)'} ...
              {'style', 'edit', 'string', ''}...
              {'style', 'text', 'string', '   Leave empty for auto-scale', 'fontangle','italic'} ...
              {'style', 'text', 'string', 'Plot confidence interval',} ...
              {'style', 'checkbox', 'string' 'on/off' 'value' 0 'tag' 'CI' }...
              {'style', 'text', 'string', '   Not available for butterfly plot (e.g. ''All'')','fontangle','italic'}...
              {} ...
              {'style', 'text', 'string', 'All files to be averaged must be in the same folder','fontweight','bold'}...
              };
             
    result = inputgui('geometry', geometry, 'uilist', uilist, 'title', 'Plot average TEPs -- pop_tesa_plotgroup()', 'helpcom', 'pophelp(''pop_tesa_plotgroup'')');
    if isempty(result), return; end;
   
    %Extract data from result
    chanInput = tepAll{1,result{1,1}};
    
    if strcmp(chanInput,'All')
        elec = [];
        tepType = 'data';
    elseif ~strncmp(chanInput,'ROI',3) && ~strncmp(chanInput,'GMF',3) && ~strcmp(chanInput,'All')
        elec = chanInput;
        tepType = 'data';
    elseif strncmp(chanInput,'ROI',3)
        tepFor = strtrim(strsplit(chanInput,' '));
        tepType = 'ROI';
        tepName = tepFor{1,2};
    elseif strncmp(chanInput,'GMF',3)
        tepFor = strtrim(strsplit(chanInput,' '));
        tepType = 'GMFA';
        tepName = tepFor{1,2};
    end
    
    xlim = str2num(result{1,2});
    
    if strcmp(result{1,3},'')
        ylim = [];
    else
        ylim = str2num(result{1,3});
    end
    
    if result{1,4} == 0
        CI = 'off';
    elseif result{1,4} == 1
        CI = 'on';
    end   
   
end

%Run script from input
if nargin > 2
    tesa_plotgroup(EEG,varargin{:});
    com = sprintf('pop_tesa_plotgroup( %s, %s );', inputname(1), vararg2str(varargin) );
elseif nargin < 2
    if strcmp(tepType,'data') && isempty(elec)
        tesa_plotgroup(EEG,'tepType',tepType,'xlim',xlim,'ylim',ylim,'CI',CI);
        com = sprintf('pop_tesa_plotgroup( %s, ''tepType'', ''%s'', ''xlim'', %s, ''ylim'', %s, ''CI'', ''%s'' );', inputname(1), tepType, mat2str(xlim), mat2str(ylim), CI);
    elseif strcmp(tepType,'data') && ~isempty(elec)
        tesa_plotgroup(EEG,'tepType',tepType,'elec',elec,'xlim',xlim,'ylim',ylim,'CI',CI);
        com = sprintf('pop_tesa_plotgroup( %s, ''tepType'', ''%s'', ''elec'', ''%s'', ''xlim'', %s, ''ylim'', %s, ''CI'', ''%s'' );', inputname(1), tepType, elec, mat2str(xlim), mat2str(ylim), CI );
    elseif strcmp(tepType,'ROI') || strcmp(tepType,'GMFA')   
        tesa_plotgroup(EEG,'tepType',tepType,'tepName',tepName,'xlim',xlim,'ylim',ylim,'CI',CI );
        com = sprintf('pop_tesa_plotgroup( %s, ''tepType'', ''%s'', ''tepName'', ''%s'', ''xlim'', %s, ''ylim'', %s, ''CI'', ''%s'' );', inputname(1), tepType, tepName, mat2str(xlim), mat2str(ylim), CI ); 
    end
end
    

end
